import random

def jogar():

    welcome_message()

    palavra_secreta = secret_word_loading()

    letras_acertadas = start_correct_letters(palavra_secreta)

    enforcou = False
    acertou = False
    erros = 0

    print(letras_acertadas)
    while (not acertou and not enforcou):

        chute = ask_kicks()

        if (chute in palavra_secreta):
            register_correct_kick(chute, palavra_secreta, letras_acertadas)
        else:
            erros += 1
            desenha_forca(erros)

        enforcou = erros == 7
        acertou = "_" not in letras_acertadas
        print(letras_acertadas)

    if (acertou):
        show_winner_message()
    else:
        show_loser_message(palavra_secreta)
    print("Fim de Jogo!")


def welcome_message():

    print('*****************************************')
    print('****** Bem-vindo ao jogo da Forca *******')
    print('*****************************************')


def secret_word_loading():
    arquivo = open("palavras.txt", "r")
    palavras = []

    for linha in arquivo:
        linha = linha.strip()
        palavras.append(linha)

    arquivo.close()

    numero = random.randrange(0, len(palavras))
    palavra_secreta = palavras[numero].upper()
    return palavra_secreta


def start_correct_letters(palavra):
    return ["_" for letra in palavra]


def ask_kicks():
    chute = input("Qual letra? ")
    chute = chute.strip().upper()
    return chute


def register_correct_kick(chute, palavra_secreta, letras_acertadas):
    index = 0
    for letra in palavra_secreta:
        if (chute == letra):
            letras_acertadas[index] = letra
        index += 1


